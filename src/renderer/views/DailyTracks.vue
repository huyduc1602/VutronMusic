  <div v-show="show">
    <div class="special-playlist1">
      <div class="title gradient">Gợi ý nhạc hàng ngày</div>
      <div class="subtitle"
        >Được tạo dựa trên sở thích âm nhạc của bạn · Cập nhật lúc 6:00 mỗi ngày</div
      >
    </div>

    <TrackList
      id="/daily/songs"
      :items="dailyTracks"
      :colunm-number="1"
      type="url"
      :is-end="true"
    />
  </div>
</template>

<script setup lang="ts">
import { ref, onMounted } from 'vue'
import { useNormalStateStore } from '../store/state'
import { storeToRefs } from 'pinia'
import TrackList from '../components/VirtualTrackList.vue'
import { dailyRecommendTracks } from '../api/playlist'

const show = ref(false)
const { dailyTracks } = storeToRefs(useNormalStateStore())

const loadDailyTracks = () => {
  dailyRecommendTracks().then((result) => {
    dailyTracks.value = result.data.dailySongs
    show.value = true
  })
}

onMounted(() => {
  if (dailyTracks.value.length === 0) {
    loadDailyTracks()
  } else {
    show.value = true
  }
})
</script>

